"""
Evaluation report generator — Stage 4.

Usage:
    python -m evaluation.report_generator \\
        --results evaluation/results/results.jsonl \\
        --output  evaluation/results/report.md
"""
import sys
import argparse
import json
from pathlib import Path
from typing import List, Dict, Any
from collections import defaultdict

sys.path.insert(0, str(Path(__file__).parent.parent))

from evaluation.metrics import score_result, aggregate_scores


def _load_jsonl(path: str) -> List[Dict[str, Any]]:
    results = []
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                results.append(json.loads(line))
    return results


def _completeness_bar(counts: dict) -> str:
    total = sum(counts.values())
    if total == 0:
        return "—"
    parts = []
    for label, n in counts.items():
        pct = round(n / total * 100)
        parts.append(f"{label}: {pct}%")
    return " | ".join(parts)


def generate_report(results_path: str, output_path: str) -> str:
    raw_results = _load_jsonl(results_path)
    if not raw_results:
        print("[Report] No results found in JSONL file.")
        return ""

    scored = [score_result(r) for r in raw_results]
    agg = aggregate_scores(scored)

    # Group by category
    by_category: Dict[str, list] = defaultdict(list)
    for r, s in zip(raw_results, scored):
        by_category[r.get("category", "unknown")].append((r, s))

    lines = []

    # ── Title ────────────────────────────────────────────────────────────────
    lines.append("# Deep Research Assistant — Evaluation Report\n")
    lines.append("*Generated by `evaluation/report_generator.py`*\n")

    # ── Summary Table ────────────────────────────────────────────────────────
    lines.append("## Summary\n")
    lines.append("| Metric | Value |")
    lines.append("|---|---|")
    lines.append(f"| Total questions | {agg['total']} |")
    lines.append(f"| Errors / failures | {agg['errors']} |")
    lines.append(f"| Success rate | {agg['success_rate'] * 100:.1f}% |")
    lines.append(f"| Avg citation presence | {agg['avg_citation_presence'] * 100:.1f}% |")
    lines.append(f"| Avg citations per answer | {agg['avg_citation_count']:.1f} |")
    lines.append(f"| Avg grounding ratio | {agg['avg_grounding_ratio']:.2f} |")
    lines.append(f"| Avg latency | {agg['avg_latency_ms']:.0f} ms |")
    lines.append(f"| Avg answer length | {agg['avg_answer_length']:.0f} chars |")
    lines.append(f"| Complete answers | {agg['complete_answers']} / {agg['total']} |")
    lines.append(f"| Uncertainty acknowledged | {agg['uncertainty_rate'] * 100:.1f}% |")
    lines.append(f"| Conflict acknowledged | {agg['conflict_rate'] * 100:.1f}% |")
    lines.append("")

    # ── Per-Category Table ───────────────────────────────────────────────────
    lines.append("## Results by Category\n")
    lines.append("| Category | N | Errors | Avg Latency (ms) | Citation % | Grounding | Completeness |")
    lines.append("|---|---|---|---|---|---|---|")

    for cat, items in sorted(by_category.items()):
        cat_scored = [s for _, s in items]
        cat_agg = aggregate_scores(cat_scored)
        completeness_counts = {
            "complete": cat_agg["complete_answers"],
            "short": cat_agg["short_answers"],
            "empty": cat_agg["empty_answers"],
        }
        lines.append(
            f"| {cat} "
            f"| {cat_agg['total']} "
            f"| {cat_agg['errors']} "
            f"| {cat_agg['avg_latency_ms']:.0f} "
            f"| {cat_agg['avg_citation_presence'] * 100:.0f}% "
            f"| {cat_agg['avg_grounding_ratio']:.2f} "
            f"| {_completeness_bar(completeness_counts)} |"
        )
    lines.append("")

    # ── Per-Question Detail Table ────────────────────────────────────────────
    lines.append("## Per-Question Results\n")
    lines.append("| ID | Category | Latency (ms) | Completeness | Citations | Grounding | Uncertainty | Conflict | Error |")
    lines.append("|---|---|---|---|---|---|---|---|---|")

    for r, s in zip(raw_results, scored):
        err_flag = "⚠️" if s["has_error"] else "✅"
        lines.append(
            f"| {s['id']} "
            f"| {s['category']} "
            f"| {s['latency_ms']:.0f} "
            f"| {s['answer_completeness']} "
            f"| {s['citation_count']} "
            f"| {s['grounding_ratio']:.2f} "
            f"| {'✓' if s['uncertainty_acknowledged'] else '✗'} "
            f"| {'✓' if s['conflict_acknowledged'] else '✗'} "
            f"| {err_flag} |"
        )
    lines.append("")

    # ── Example Answers ──────────────────────────────────────────────────────
    lines.append("## Example Answers\n")

    # Best answer (most citations)
    best = max(zip(raw_results, scored), key=lambda x: x[1]["citation_count"], default=None)
    if best:
        br, _ = best
        lines.append(f"### ✅ Example: Well-cited Answer (`{br['id']}`)\n")
        lines.append(f"**Q:** {br['question']}\n")
        snippet = (br.get("answer") or "")[:800]
        if len(br.get("answer") or "") > 800:
            snippet += "…"
        lines.append(f"**A (excerpt):** {snippet}\n")

    # Uncertainty example
    uncertain = next(
        ((r, s) for r, s in zip(raw_results, scored) if s["uncertainty_acknowledged"]),
        None
    )
    if uncertain:
        ur, _ = uncertain
        lines.append(f"### 🔍 Example: Uncertainty Handling (`{ur['id']}`)\n")
        lines.append(f"**Q:** {ur['question']}\n")
        snippet = (ur.get("answer") or "")[:600]
        if len(ur.get("answer") or "") > 600:
            snippet += "…"
        lines.append(f"**A (excerpt):** {snippet}\n")

    # Error example
    err_ex = next(((r, s) for r, s in zip(raw_results, scored) if s["has_error"]), None)
    if err_ex:
        er, es = err_ex
        lines.append(f"### ❌ Example: Failed Question (`{er['id']}`)\n")
        lines.append(f"**Q:** {er['question']}\n")
        lines.append(f"**Error:** {er.get('error', 'unknown')}\n")

    # ── Metrics Explanation ──────────────────────────────────────────────────
    lines.append("## Metrics Explanation\n")
    lines.append("| Metric | Definition |")
    lines.append("|---|---|")
    lines.append("| Citation Presence | 1.0 if answer contains at least one `[Sn]` marker |")
    lines.append("| Citation Count | Number of unique `[Sn]` markers in the answer |")
    lines.append("| Grounding Ratio | Fraction of sentences containing a citation marker |")
    lines.append("| Answer Completeness | `complete` ≥200 chars, `short` <200 chars, `empty` |")
    lines.append("| Uncertainty Acknowledged | Answer contains phrases like 'insufficient', 'unclear', etc. |")
    lines.append("| Conflict Acknowledged | Answer contains phrases like 'however', 'on the other hand', etc. |")
    lines.append("| Latency | Wall-clock ms from query submission to result return |")
    lines.append("")

    report = "\n".join(lines)
    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(report)

    print(f"[Report] Report written to {output_path}")
    return report


def main():
    parser = argparse.ArgumentParser(description="Generate evaluation report from JSONL results")
    parser.add_argument("--results", required=True, help="Path to results.jsonl")
    parser.add_argument("--output", default="evaluation/results/report.md", help="Output Markdown path")
    args = parser.parse_args()
    generate_report(args.results, args.output)


if __name__ == "__main__":
    main()
